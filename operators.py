def add(n1, n2):
    '''Returns a sum of two numbers'''
    return n1 + n2

def subtract(n1, n2):
    '''Returns the difference between two numbers'''
    return n1 - n2

def multiply(n1, n2):
    '''Returns the product of two numbers'''
    return n1 * n2

def divide(n1, n2):
    '''Returns as a float how many times n2 goes into n1'''
    return n1 / n2